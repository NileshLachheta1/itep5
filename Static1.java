// program showing the concept of static keyword
/*
        static Keyword : 
            a) Variable
            b) Method
            c) Class
            d) Block
*/
class Static1
{
    static{
        System.out.println("Statement 1");
    }
    public static void main(String args[])
    {
        System.out.println("Statement 2");
    }
    static{
        System.out.println("Statement 3");
    }
}





