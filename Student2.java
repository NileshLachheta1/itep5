class Student2
{
    /* instance variable | data members */
    private int rno;
    private String name;
    private double per;

    /* setter methods */
        public void setRno(int rno)
        {
            this.rno=rno;
        }
        public void setName(String name)
        {
            this.name=name;
        }
        public void setPer(double per)
        {
            this.per=per;
        }
    /* getter methods */
        public int getRno()
        {
            return rno;
        }
        public String getName()
        {
            return name;
        }
        public double getPer()
        {
            return per;
        }
}
